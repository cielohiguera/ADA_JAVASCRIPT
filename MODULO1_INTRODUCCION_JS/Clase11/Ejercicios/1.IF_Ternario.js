// Ejercicio: Verificar si una persona es mayor de edad
// Paso 1: declaramos la variable de la edad de la persona
let edad = 18;
// Paso 2: Usamos el operador ternario para determinar si es mayor de edad
let mensaje = edad >= 18 ? "Mayor de edad" : "Menor de edad";
// La condición es: Edad es mayor o igual a 18 ?
// Si es verdadera, el resultado será "Menor de edad"
// Si es falsa, el resultado será "Menor de edad"

// Paso 3: Mostramos por consola
console.log(mensaje);

// Explicación:
// El operador ternario funciona como IF pero en una sola línea
// La estructura: condicion ? valor_si_verdadero : valor_si_falso
// En este caso:
// condición: edad >= 18
// valor_si_verdadero: "Mayor de edad"
// valor_si_falso: "Menor de edad"