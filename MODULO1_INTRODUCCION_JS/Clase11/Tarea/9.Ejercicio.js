// Ejercicio 9: cuenta regresiva para el despegue
// Crear simular la cuenta regresiva para el lanzamiento de un cohete. El programa debe contar desde 10 hasta 0 e imprimir 
// ¡Despegue! al final
// 
console.log("Iniciando la cuenta regresiva para el despegue..");
// Ciclo para realizar la cuenta regresiva
for (let i = 10; i >= 0; i--) {
    console.log(i);
}
// Mensaje final
console.log("¡Despegue!");