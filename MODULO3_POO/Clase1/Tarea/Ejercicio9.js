"use strict";
// Ejercicio 9: Comparaciones lógicas
// Declara dos variables booleanas y realiza una comparación lógica (como AND,
// OR). Imprime el resultado.
// Declaración de dos variables booleanas
let variable1 = true;
let variable2 = false;
// Realizar una comparación lógica con AND
let resultadoAND = variable1 && variable2;
// Realizar una comparación lógica con OR
let resultadoOR = variable1 || variable2;
// Imprimir los resultados
console.log("Resultado de la comparación AND:", resultadoAND);
console.log("Resultado de la comparación OR:", resultadoOR);
