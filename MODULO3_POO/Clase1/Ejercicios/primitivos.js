"use strict";
// Ejemplos de tipos de primitivos de TypeScript
// 1. String
let nombre = 'Ada Lovelace';
let saludo = `Hola ${nombre}, bienvenida a TypeScript`;
console.log(saludo);
// 2. Number 
let entero = 42;
let decimal = 3.14;
let cientifico = 1e5;
console.log("Entero", entero);
console.log("Decimal", decimal);
console.log("Cientifico", cientifico);
// 3. Boolean
let esEstdiante = true;
let tienesLicencia = false;
console.log("¿Es estudiante", esEstdiante);
console.log("¿Tienes licencia?", tienesLicencia);
// 4. null 
let valorNull = null;
console.log("Valor nulo:", valorNull);
// 5. undifined
let valorIndefinido = undefined;
console.log("Valor indefinido:", valorIndefinido);
